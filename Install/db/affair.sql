CREATE TABLE IF NOT EXISTS `affair` (
  `AFF_ID` int(11) NOT NULL AUTO_INCREMENT,
  `USER_ID` varchar(20) NOT NULL DEFAULT '',
  `BEGIN_TIME` int(10) unsigned NOT NULL,
  `END_TIME` int(10) unsigned NOT NULL,
  `TYPE` char(1) NOT NULL DEFAULT '',
  `REMIND_DATE` varchar(200) NOT NULL DEFAULT '',
  `REMIND_TIME` time NOT NULL DEFAULT '00:00:00',
  `CONTENT` text NOT NULL,
  `LAST_REMIND` date NOT NULL DEFAULT '0000-00-00',
  `SMS_REMIND` char(1) NOT NULL DEFAULT '1',
  `SMS2_REMIND` varchar(2) NOT NULL,
  `LAST_SMS2_REMIND` date NOT NULL DEFAULT '0000-00-00',
  `MANAGER_ID` varchar(20) NOT NULL,
  `CAL_TYPE` char(1) NOT NULL,
  `ADD_TIME` datetime NOT NULL,
  PRIMARY KEY (`AFF_ID`),
  KEY `USER_ID` (`USER_ID`),
  KEY `BEGIN_TIME_2` (`BEGIN_TIME`),
  KEY `END_TIME_2` (`END_TIME`)
);